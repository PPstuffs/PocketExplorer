#include "../../include/header_lib.h"

float my_add(float a, float b)
{
    return a + b;
}

float my_sub(float a, float b)
{
    return a - b;
}

float my_mul(float a, float b)
{
    return a * b;
}

float my_div(float a, float b)
{
    return a / b;
}
